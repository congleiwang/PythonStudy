# chr()函数把编码转换为对应的字符
# encode()方法可以编码为指定的字节数据
# decode()方法，把bytes变为指定编码的str
# len()函数计算的是str的字符数，如果换成bytes，len()函数就计算字节数
# 在Python中，采用的格式化方式和C语言是一致的，用%实现，用%转义
# %d 	整数
# %f 	浮点数
# %s 	字符串
# %x 	十六进制整数